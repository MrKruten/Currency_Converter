interface DataGraphType {
	name: string;
	currencies: { [key in string]: number };
}

export interface IGraphCurrency {
	data: Array<DataGraphType>;
}

// prototype
export const GraphCurrency = () => {
	const createDataGraph = (currenciesOfPeriod: IResponseHistoricalCurrency) => {
		return Object.entries(currenciesOfPeriod.data).map(data => ({
			name: data[0],
			...data[1],
		}));
	};
	const dataGraph = createDataGraph(data);
	return (
		<LineChart
			width={600}
			height={300}
			data={dataGraph}
			margin={{ top: 5, right: 20, bottom: 5, left: 0 }}
		>
			<Line type='monotone' dataKey='RUB' stroke='#8884d8' />
			<CartesianGrid stroke='#ccc' strokeDasharray='5 5' />
			<XAxis dataKey='name' />
			<YAxis />
			<Legend />
			<Tooltip />
		</LineChart>
	);
};
